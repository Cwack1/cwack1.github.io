#Legg sammen to tall
def addisjon(tall1, tall2):
    return tall1 + tall2

#Subtraksjon
def subtraksjon(tall1, tall2):
    return tall1 - tall2

#Hvordan bruke funksjonene:
svar = addisjon(10, 5)
#Skriv ut svaret på addisjonsstykket
print(svar)

#Erstatt svar med svaret på subtraksjonsstykket
svar = subtraksjon(10, 5)
#Skriv ut svaret fra subtraksjonsstykket
print(svar)